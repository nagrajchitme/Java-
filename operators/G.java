package operators;

public class G 
{  //increment and decrement
	public static void main(String[] args) 
	{
		int a,b;
		a=10;
		b=20;
		System.out.println(a--);
		System.out.println(--a);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(b++);
		System.out.println(b--);
	}
}
